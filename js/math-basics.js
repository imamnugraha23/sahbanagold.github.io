function pangkatAngka(angka,pangkat){
	return Math.pow(angka,pangkat);
}

function akarKuadrat(angka) {
	return Math.sqrt(angka);
}

function hipotenusa(sisi1,sisi2) {
	return Math.sqrt(Math.pow(sisi1,2) + Math.pow(sisi2,2));
}

function luasLingkaran (jari2) {
	return Math.PI * Math.pow(jari2,2);
}